DO $$
BEGIN
    IF NOT EXISTS (
        SELECT 1
        FROM pg_constraint
        WHERE conname = 'fk_tracking_goals_target_macros'
          AND conrelid = 'public.tracking_goals'::regclass
    ) THEN
        ALTER TABLE tracking_goals
            ADD CONSTRAINT fk_tracking_goals_target_macros
            FOREIGN KEY (target_macros) REFERENCES macronutrient_values (id);
    END IF;

    IF NOT EXISTS (
        SELECT 1
        FROM pg_constraint
        WHERE conname = 'fk_trackings_goal'
          AND conrelid = 'public.trackings'::regclass
    ) THEN
        ALTER TABLE trackings
            ADD CONSTRAINT fk_trackings_goal
            FOREIGN KEY (tracking_goal_id) REFERENCES tracking_goals (id);
    END IF;

    IF NOT EXISTS (
        SELECT 1
        FROM pg_constraint
        WHERE conname = 'fk_trackings_consumed_macros'
          AND conrelid = 'public.trackings'::regclass
    ) THEN
        ALTER TABLE trackings
            ADD CONSTRAINT fk_trackings_consumed_macros
            FOREIGN KEY (consumed_macros_id) REFERENCES macronutrient_values (id);
    END IF;

    IF NOT EXISTS (
        SELECT 1
        FROM pg_constraint
        WHERE conname = 'fk_tracking_meal_plan_entries_type'
          AND conrelid = 'public.tracking_meal_plan_entries'::regclass
    ) THEN
        ALTER TABLE tracking_meal_plan_entries
            ADD CONSTRAINT fk_tracking_meal_plan_entries_type
            FOREIGN KEY (mealplan_type) REFERENCES mealplan_types (id);
    END IF;

    IF NOT EXISTS (
        SELECT 1
        FROM pg_constraint
        WHERE conname = 'fk_tracking_meal_plan_entries_tracking'
          AND conrelid = 'public.tracking_meal_plan_entries'::regclass
    ) THEN
        ALTER TABLE tracking_meal_plan_entries
            ADD CONSTRAINT fk_tracking_meal_plan_entries_tracking
            FOREIGN KEY (tracking_id) REFERENCES trackings (id);
    END IF;
END $$;
