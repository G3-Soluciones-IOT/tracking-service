CREATE TABLE IF NOT EXISTS macronutrient_values (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    calories DOUBLE PRECISION NOT NULL,
    carbs DOUBLE PRECISION NOT NULL,
    proteins DOUBLE PRECISION NOT NULL,
    fats DOUBLE PRECISION NOT NULL
);

CREATE TABLE IF NOT EXISTS mealplan_types (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(12)
);

CREATE UNIQUE INDEX IF NOT EXISTS uk_mealplan_types_name
    ON mealplan_types (name);

CREATE TABLE IF NOT EXISTS tracking_goals (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL,
    target_macros BIGINT,
    created_at TIMESTAMP(6) WITH TIME ZONE NOT NULL,
    updated_at TIMESTAMP(6) WITH TIME ZONE NOT NULL
);

CREATE TABLE IF NOT EXISTS trackings (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    tracking_date DATE NOT NULL,
    user_id BIGINT NOT NULL,
    tracking_goal_id BIGINT NOT NULL,
    consumed_macros_id BIGINT,
    created_at TIMESTAMP(6) WITH TIME ZONE NOT NULL,
    updated_at TIMESTAMP(6) WITH TIME ZONE NOT NULL
);

CREATE TABLE IF NOT EXISTS tracking_meal_plan_entries (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    recipe_id BIGINT NOT NULL,
    mealplan_type BIGINT,
    day_number INTEGER,
    tracking_id BIGINT NOT NULL,
    created_at TIMESTAMP(6) WITH TIME ZONE NOT NULL,
    updated_at TIMESTAMP(6) WITH TIME ZONE NOT NULL
);

CREATE TABLE IF NOT EXISTS water_intakes (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL,
    amount_ml INTEGER NOT NULL,
    intake_date DATE NOT NULL,
    registered_at TIMESTAMP(6) NOT NULL,
    created_at TIMESTAMP(6) WITH TIME ZONE NOT NULL,
    updated_at TIMESTAMP(6) WITH TIME ZONE NOT NULL
);

DO $$
DECLARE
    seed_name TEXT;
BEGIN
    FOREACH seed_name IN ARRAY ARRAY['BREAKFAST', 'LUNCH', 'DINNER', 'HEALTHY']
    LOOP
        INSERT INTO mealplan_types (name)
        SELECT seed_name
        WHERE NOT EXISTS (SELECT 1 FROM mealplan_types WHERE mealplan_types.name = seed_name);
    END LOOP;
END $$;

